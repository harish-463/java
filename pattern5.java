class pattern4
{
    public static void main(String args[])
    {
        int i,n=5,j;
        for(i=1;i<=n;i++)
        {
            for(j=5;j>i-1;j--)
            {
                System.out.print("*");
            }
            {
                System.out.println(" ");
}
}
    }
}