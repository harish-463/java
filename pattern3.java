class pattern2
{
    public static void main(String args[])
    {
        int i,n=5,j,k;
        for(i=1;i<=n;i++)
        {
            for(j=n;j>i;j--)
            {
                System.out.print(" ");
            }
            for(k=1;k<=i;k++)
            {
                System.out.print("*");
            }
            System.out.println("");
}
}
}
